if __name__ == "__main__":
    from argparse import ArgumentParser
    ap = ArgumentParser()
    ap.add_argument("-m", type=int, default=1000000, help="Number of objects")
    ap.add_argument("-n",
                    type=int,
                    default=200000000,
                    help="Number of requests")
    ap.add_argument("--alpha", type=float, default=1.0, help="Zipf parameter")
    ap.add_argument("--bin-output",
                    type=str,
                    default="",
                    help="Output to a file (oracleGeneral format)")
    ap.add_argument("--obj-size",
                    type=int,
                    default=4000,
                    help="Object size (used when output to a file)")
    ap.add_argument("--time-span",
                    type=int,
                    default=86400 * 7,
                    help="Time span of all requests in seconds")
    ap.add_argument("--zipf-ratio", type=float, default=0.6, help="Zipf distributed accessed in m")
    ap.add_argument("--churn-ratio", type=float, default=0.6, help="Total churn as a fraction of m")
    ap.add_argument("--uniform-ratio", type=float, default=0.6, help="Uniformly and random distributed accesses as a fraction of m")
    ap.add_argument("--uniform-multiplier", type=int, default=20, help="Number of uniform requests is this multiplier times the uniform-ratio * m")
    ap.add_argument("--frequent-ratio", type=float, default=0.2, help="Frequently accessed objects as a fraction of m")
    ap.add_argument("--frequent-repeats", type=int, default=30, help="Number of repeats for frequently accessed objects")
    ap.add_argument("--scan-ratio", type=float, default=0.6, help="Scan-like accesses as a fraction of m")
    ap.add_argument("--scan-repeats", type=int, default=1, help="Number of repeats for each large scan segment")

    p = ap.parse_args()

    output_file = open(p.bin_output, "wb") if p.bin_output else None
    request_record = struct.Struct("<IQIq")

    request_index = 0
    (warmup_requests, pattern_requests, scan_size, scan_base) = build_workload_segments(p)

    current_scan = gen_repeated_range(scan_size, 1, scan_base)

    request_index = write_requests(warmup_requests, request_index, output_file,
                                   request_record, p.obj_size, p.time_span, p.n)
    request_index = write_requests(current_scan, request_index, output_file, request_record,
                                   p.obj_size, p.time_span, p.n)

    while request_index < p.n:
        request_index = write_requests(pattern_requests, request_index,
                                       output_file, request_record, p.obj_size,
                                       p.time_span, p.n)
        request_index = write_requests(current_scan, request_index, output_file,
                                       request_record, p.obj_size, p.time_span,
                                       p.n)
        scan_base += scan_size
        current_scan = gen_repeated_range(scan_size,
                                       1,
                                       scan_base)

    if output_file:
        output_file.close()
